package com.shopping.model.bean;

public class Combo04 {
	private String id;
	private int total;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
	@Override
	public String toString() {
		return "Combo04 [id=" + id + ", total=" + total + "]";
	}

}
